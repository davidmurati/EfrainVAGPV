// ---------- i18n Engine ----------

let currentLang = 'es';

function applyTranslations(lang) {
  currentLang = lang;
  const dict = translations[lang];
  if (!dict) return;

  // Update text content via data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Update placeholders via data-i18n-ph
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (dict[key] !== undefined) {
      el.placeholder = dict[key];
    }
  });

  // Update <html lang>
  document.documentElement.lang = lang;

  // Save preference
  localStorage.setItem('agpv-lang', lang);

  // Update button states
  const btnEs = document.getElementById('langEs');
  const btnEn = document.getElementById('langEn');
  if (btnEs && btnEn) {
    btnEs.classList.toggle('active', lang === 'es');
    btnEn.classList.toggle('active', lang === 'en');
  }
}

function detectPreferredLanguage() {
  // 1. Check localStorage
  const saved = localStorage.getItem('agpv-lang');
  if (saved && translations[saved]) return saved;

  // 2. Check browser language
  const browserLang = (navigator.language || navigator.userLanguage || '').substring(0, 2).toLowerCase();
  if (browserLang === 'en') return 'en';

  // 3. Default to Spanish
  return 'es';
}

// ---------- Init ----------
document.addEventListener('DOMContentLoaded', () => {
  const btnEs = document.getElementById('langEs');
  const btnEn = document.getElementById('langEn');

  if (btnEs) {
    btnEs.addEventListener('click', () => applyTranslations('es'));
  }
  if (btnEn) {
    btnEn.addEventListener('click', () => applyTranslations('en'));
  }

  // Auto-detect and apply
  const preferred = detectPreferredLanguage();
  if (preferred !== 'es') {
    applyTranslations(preferred);
  }
});

